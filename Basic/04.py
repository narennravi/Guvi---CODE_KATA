# Question:-
'''
Write a code to get the input and print it 5 times.

Input Description:
A single line contains an integer N.

Output Description:
Output contains 5 lines with each line having the value N.

Sample Input :
4
Sample Output :
4
4
4
4
4
'''


# Solution:-
num = int(input())
i=0
while i<5:
    print(num)
    i+=1