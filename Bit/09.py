# Question:-
'''
You are given a number ‘n’ you have to tell whether number is divisible by 8.

Print 1 if it is divisible and 0 if it is not.

Input Description:
You are given a number ‘n’.

Output Description:
Print 1 if number is divisible by 8 and 0 if not

Sample Input :
16
Sample Output :
1
'''



# Solution:-
n = int(input())
if n % 8 == 0: print(1)
else: print(0)